# FUNÇÕES LISTAS
lista = ['cachorro', 'gato', 'pássaro']
lista2 = [2, 4, 6]

# 1. LEN
# Saber quantos elementos tem na lista
print(len(lista))

# 2. SUM
# Somar todos os valores da lista.
print(sum(lista2))

# 3. MAX
# Retonar o maior valor da lista.
print(max(lista2))

# 4. MIN
# Retonar o menor valor da lista.
print(min(lista2))