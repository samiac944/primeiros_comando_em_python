# OPERADORES ARITMÉTICOS

"""
    OPERADORES MATEMÁTICOS

    Soma: +
    Subtração: -
    Multiplicação: *
    Divisão: /
    Divisão inteira: // (ignora o valor decimal)
    Resto de divisão: % ()
    Potência: **

"""
numero1 = 15
numero2 = 2

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(numero1 % numero2)
print(numero1 ** numero2)

# OPERADORES BOOLEANOS (operadores de comparação)

idade1 = 10
idade2 = 25
altura1 = 1.60
altura2 = 1.75
altura3 = altura2

print(idade1 > idade2)
print(idade1 <= idade2)
print(altura1 >= altura2)
print(altura3 > altura2)

# O operador igual a ( == ) retorna true se os dois operandos
#  tiverem o mesmo valor; caso contrário, retorna false 
print("Samia" == "Sâmia")

# O operador diferente de ( != ) retorna true se os operandos 
# não tiverem o mesmo valor; caso contrário, retorna false 
print("Luiz" != "luiz")